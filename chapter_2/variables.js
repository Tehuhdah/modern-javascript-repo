// Using let is the modern way to declare variables.
let age = 25;
let year = 2023;
console.log(age, year);

// Reassigning the age variable to a new value.
age = 30;
console.log(age);

// Using const is the modern way to declare variables that cannot change its value.
const points = 100;
// Trying to reassign the points variable to a new value.
points = 90;
// This should cause an error.
console.log(points);

// Using var is the old way to declare variables.
var score = 75;
console.log(score);

